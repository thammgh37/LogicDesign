void set_state_matrix(){
	if(mode_char == A && mode_animation == stable) state_led_matrix = A_stable;
	if(mode_char == A && mode_animation == run) state_led_matrix = A_run;

	if(mode_char == B && mode_animation == stable) state_led_matrix = B_stable;
	if(mode_char == B && mode_animation == run) state_led_matrix = B_run;

}

void displayLedMatrix(int index1, int index2){
	set_state_matrix();
	switch(state_led_matrix){
	case A_stable:
		display_col(index2);
		display_row(a_stay[0][index2-1]);
		break;
	case A_run:
		display_col(index2);
		display_row(a_run[index1][index2-1]);
		break;


	case B_stable:
		display_col(index2);
		display_row(b_stay[0][index2-1]);
		break;
	case B_run:
		display_col(index2);
		display_row(b_run[index1][index2-1]);
		break;

	default:
		break;
	}
}
